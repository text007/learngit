
# 字符串 
# 使用引号( ' 或 " )来创建字符串

print('123')
print("字符串")
print('------------------------------')

# 访问字符串中的值
var1 = 'hello world!'
var2 = "123456"

print(var1[0])
print(var2[1:5])
print('------------------------------')

# 字符串更新
var1 = 'hello world!'
var2 = "123456"

print(var1[:6] + var2[:3])
print('------------------------------')
